void main(List<String> args) {
  String grade = 'A';
  switch (grade) {
    case 'A':
      print('Grade is A');
      break;
    case 'B':
      print('Grade is B');
      break;
    case 'C':
      print('Grade is C');
      break;
    default:
      print('Grade is D');
  }
}
